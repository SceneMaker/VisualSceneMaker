package de.dfki.vsm.editor.util.grid;

/**
 *
 * @author Souza Putra
 */
public class BezierPoint {
    double x, y;

    public BezierPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
